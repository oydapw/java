package org.example;

public class Otdel {
    private String name;
    private int staff;
    public Otdel(String name, int staff)
    {
        this.name = name;
        this.staff = staff;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public int getStaff()
    {
        return staff;
    }
    public void setStaff(int staff)
    {
        this.staff = staff;
    }






}
